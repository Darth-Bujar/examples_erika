/* This function initializes the port pin which drives the LED */
void initLED(void)
{
    /* Initialization of the LED used in this example */
    IfxPort_setPinModeOutput(LED_D107, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
//    IfxPort_setPinModeOutput(LED_RESET, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
    IfxPort_setPinModeInput(LED_PWDN, IfxPort_InputMode_pullUp);
    IfxPort_setPinModeInput(LED_RESET, IfxPort_InputMode_pullUp);
    IfxPort_setPinModeInput(RXD2, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(RXD1, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(RDX0, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(RCTL, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(RXCLK, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(TXD3, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(TXD2, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(TXD1, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(TXD0, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(TXCLK, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(REFCLK, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(TCTL, IfxPort_InputMode_noPullDevice);
    IfxPort_setPinModeInput(RXD3, IfxPort_InputMode_noPullDevice);


//    IfxPort_setPinModeOutput(LED_PWDN, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
//    IfxPort_setPinModeOutput(LED_PWDN, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);

    /* Switch OFF the LED (low-level active) */
    IfxPort_setPinHigh(LED_D107);
//    IfxPort_setPinHigh(LED_RESET);
//    IfxPort_setPinHigh(LED_PWDN);
}

