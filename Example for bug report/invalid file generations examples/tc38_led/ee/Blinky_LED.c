#define LED_D107    &MODULE_P15,0                                           /* LED D107: Port, Pin definition       */
#define LED_RESET   &MODULE_P34,4                                           /* LED D107: Port, Pin definition       */
#define LED_PWDN    &MODULE_P11,15                                           /* LED D107: Port, Pin definition       */
#define WAIT_TIME   500                                                     /* Wait time constant in milliseconds   */
#define RXD2        &MODULE_P11,8
#define RXD1        &MODULE_P11,9
